@dataclass
class Book:
    """Represents a single book title held in the library catalog."""
    book_id: int
    title: str
    author: str
    category: BookCategory
    publisher: str
    quantity: int
    status: BookStatus = field(init=False)
    created_at: datetime = field(default_factory=datetime.now)
    updated_at: datetime = field(default_factory=datetime.now)

    def __post_init__(self):
        # Derive initial status from the supplied quantity
        self.status = self._compute_status(self.quantity)

    @staticmethod
    def _compute_status(quantity: int) -> BookStatus:
        """Return IN_STOCK when at least one copy is available, OUT_OF_STOCK otherwise."""
        return BookStatus.IN_STOCK if quantity > 0 else BookStatus.OUT_OF_STOCK

    def refresh_status(self):
        """
        Recompute and persist the book's status based on its current quantity.
        Called after every mutation to satisfy Postcondition 6.5:
        'The book information, quantity, and status are updated successfully.'
        """
        self.status = self._compute_status(self.quantity)
        self.updated_at = datetime.now()

    def __str__(self) -> str:
        return (f"[{self.book_id}] '{self.title}' - {self.author} | "
                f"Category: {self.category.name} | Publisher: {self.publisher} | "
                f"Qty: {self.quantity} | Status: {self.status.value}")


